function funcao() {
    var difficult = prompt("Digite o nível de dificuldade (fácil, médio ou difícil").toLowerCase()
    if (difficult == "facil") {
        document.write("Você escolheu o nível fácil!")
    } else if (difficult == "medio") {
        document.write("Você escolheu o nível médio!")
    } else if (difficult == "dificil") {
        document.write("Você escolheu o nível difícil!")
    }
}